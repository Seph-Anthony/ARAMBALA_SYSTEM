    private void verifysecurityMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_verifysecurityMouseClicked
     // 1. Get all answers from components
    SessionClass ses = SessionClass.getInstance();
    String userEmail = ses.getEmail();
    
    String colorAnswer = (String)favoritecolor.getSelectedItem();
    String subjectAnswer = (String)favoritesub.getSelectedItem();
    String nicknameAnswer = childhoodname.getText().trim();
    
    if(nicknameAnswer.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Please enter your childhood nickname", 
            "Missing Information", 
            JOptionPane.WARNING_MESSAGE);
        childhoodname.requestFocus();
        return;
    }
    
    try {
        dbConnect db = new dbConnect();
        String sql = "SELECT u_id, u_type, u_username, u_fname, u_lname, u_contact FROM user WHERE " +
                     "u_email = '" + userEmail + "' AND " +
                     "u_ans1 = '" + colorAnswer + "' AND " +
                     "u_ans2 = '" + subjectAnswer + "' AND " +
                     "u_ans3 = '" + nicknameAnswer + "'";
        
        ResultSet rs = db.getData(sql);
        
        if(rs.next()) {
            // Store ALL user data in session
            SessionClass sess = SessionClass.getInstance();
            sess.setU_id(rs.getInt("u_id"));
            sess.setType(rs.getString("u_type"));
            sess.setUsername(rs.getString("u_username"));
            sess.setFname(rs.getString("u_fname"));
            sess.setLname(rs.getString("u_lname"));
            sess.setContact(rs.getString("u_contact"));
            
            
            newpass ne = new newpass();
            ne.setVisible(true);
            
            
            // Redirect based on user type
//            switch(sess.getType().toLowerCase()) {
//                case "admin":
//                    new admindash().setVisible(true);
//                    break;
//                case "employee":
//                    new employdash().setVisible(true);
//                    break;
//                case "customer":
//                    new customerdashboard().setVisible(true);
//                    break;
//                default:
//                    JOptionPane.showMessageDialog(this,
//                        "Unknown user type",
//                        "Error",
//                        JOptionPane.ERROR_MESSAGE);
//                    return;
//            }
            
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this,
                "Incorrect security answers",
                "Verification Failed",
                JOptionPane.ERROR_MESSAGE);
        }
    } catch(SQLException ex) {
        JOptionPane.showMessageDialog(this,
            "Database error: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_verifysecurityMouseClicked

